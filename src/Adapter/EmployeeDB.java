package Adapter;

public class EmployeeDB {
    private int id;
    private String fName;
    private String lName;
    private String email;

    public EmployeeDB(int id, String fName, String lName, String email) {
        this.id = id;
        this.fName = fName;
        this.lName = lName;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public String getfName() {
        return fName;
    }

    public String getlName() {
        return lName;
    }

    public String getEmail() {
        return email;
    }
}
